class Loops
{
public static void main(String []args)
{


for(int i =1 ; i<=10 ;i++)
{
System.out.println(i+" ");
}

for(char ch ='A' ; ch<='Z' ;ch++)
{
System.out.println(ch+" ");
}


for(char ch ='z' ; ch>='a' ;ch--)
{
System.out.println(ch+" ");
}


for(char ch ='0' ; ch<='9' ;ch++)
{
System.out.println(ch+" ");
}


for(int i =1 ; i<=127 ;i++)
{
System.out.println(i+" :"+((char)i));
}


}
}